func TestUnpack(t *testing.T) {
	tests := []struct {
		input    string
		expected string
	}{
		{input: "a4bc2d5e", expected: "aaaabccddddde"},
		{input: "abccd", expected: "abccd"},
		{input: "", expected: ""},
		{input: "aaa0b", expected: "aab"},
		{input: "ðŸ™ƒ0", expected: ""},
		{input: "aaÑ„0b", expected: "aab"},
		{input: "d\n5abc", expected: "d\n\n\n\n\nabc"},
		{input: "Ð°2Ð±Ð±Ð±Ð²4Ð³Ð³Ð³Ð³Ð³Ð´6", expected: "Ð°Ð°Ð±Ð±Ð±Ð²Ð²Ð²Ð²Ð³Ð³Ð³Ð³Ð³Ð´Ð´Ð´Ð´Ð´Ð´"},
		{input: "à§§à§¨à§©", expected: "à§§à§¨à§©"},
		{input: "à§§2à§¨à§©0", expected: "à§§à§§à§¨"},
		{input: "à©©4", expected: "à©©à©©à©©à©©"},
	}

	for _, tc := range tests {
		t.Run(tc.input, func(t *testing.T) {
			result, err := Unpack(tc.input)
			require.NoError(t, err)
			require.Equal(t, tc.expected, result)
		})
	}
}

func TestUnpackInvalidString(t *testing.T) {
	invalidStrings := []string{"3abc", "45", "aaa10b"}
	for _, tc := range invalidStrings {
		t.Run(tc, func(t *testing.T) {
			_, err := Unpack(tc)
			require.Truef(t, errors.Is(err, ErrInvalidString), "actual error %q", err)
		})
	}
}

func TestUnpackAsterisk(t *testing.T) {
	tests := []struct {
		input    string
		expected string
	}{
		{input: `qwe\4\5`, expected: `qwe45`},
		{input: `qwe\45`, expected: `qwe44444`},
		{input: `qwe\\5`, expected: `qwe\\\\\`},
		{input: `qwe\\\3`, expected: `qwe\3`},
	}

	for _, tc := range tests {
		t.Run(tc.input, func(t *testing.T) {
			result, err := UnpackAsterisk(tc.input)
			require.NoError(t, err)
			require.Equal(t, tc.expected, result)
		})
	}
}

func TestUnpackAsteriskInvalidString(t *testing.T) {
	invalidStrings := []string{`3abc`, `qw\ne`}
	for _, tc := range invalidStrings {
		t.Run(tc, func(t *testing.T) {
			_, err := UnpackAsterisk(tc)
			require.Truef(t, errors.Is(err, ErrInvalidString), "actual error %q", err)
		})
	}
}
